    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //TODO -> CREAR PELICULA EN BBDD
        String titulo = this.jTFTitulo.getText();
        int anio = Integer.parseInt(this.jSpinnerAnno.getValue().toString());
        String fechaEmision = UtilesFecha.spinnerFechaToStringDate(
                this.jSpinnerFechaEm.getValue());
        Date fechaDateEmision = UtilesFecha.fechaStrToDate(fechaEmision);
        String genero = this.jComboBoxGenero.getSelectedItem().toString();
        //String rutaImg = this.customJLabelImagenPeli.getImgRoot();
        
        //Si la fecha actual ya ha sobrepasado la fecha de emisión que hemos puesto, avisar al Usuario
        if (new Date().after(fechaDateEmision)) {
            Vistas.mostrarErrorGUI(rootPane, "FECHA NO VÁLIDA!", "La fecha de emisión que ha introducido para la película YA HA EXPIRADO! Introduzca una fecha posterior válida");
            return;
        }
        if (ControladorPeliculas.existePelicula(titulo, anio)) {
            Vistas.mostrarErrorGUI(rootPane, "PELÍCULA IDÉNTICA YA GUARDADA", "La película del año que ha intentado\n"
                    + " ingresar ya se encuntra en el Sistema."
                    + "Cambie los datos e inténtelo de nuevo...");
            return;
        }
        Pelicula pelicula = new Pelicula(0, titulo, genero, fechaEmision, anio);
        try {
            ControladorPeliculas.agregarPelicula(pelicula);
        } catch (PeliculaYaExisteException peliYaExisteErr) {
            JOptionPane.showMessageDialog(rootPane, peliYaExisteErr.getMessage(), "LA PEL�?CULA YA EXISTE!", JOptionPane.ERROR_MESSAGE);
        }
        
        //Actualizar la tabla de películas, y recargar la página
        padre.actualizarListaESTRENOS();
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    /**
     * Al perder el FOCO, verificar si ya existe una película con el titulo introducido.
     * Si existe, mostrar el JLabel de Error
     * @param evt 
     */
    private void jTFTituloFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTFTituloFocusLost
        if (jTFTitulo.getText().isBlank() || jTFTitulo.getText().isBlank()) {
            jLabelError.setVisible(false);
            return;
        }
        //Sólo hacer aparecer el mensaje de error si existe una película con el titulo establecido.
        if (ControladorPeliculas.getPeliculaByTitulo(jTFTitulo.getText()) != null) {
            jLabelError.setVisible(true);
            return;
        }
        jLabelError.setVisible(false);
    }//GEN-LAST:event_jTFTituloFocusLost

