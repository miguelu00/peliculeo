    public void prepararCompPersonalizado() {
        this.imgFondo2 = new ImagenFondo2(new File("src/GUI/imgs/placeholder-img.png").getAbsoluteFile(), 1.0f);
        this.jPanelImagenFondoArrastable1.addArrastreListener(
        new gui_eventospersonalizado.ArrastreListener() {
            //Si arrastramos, preguntar si descartar la imágen.
            @Override
            public void arrastre() {
                //throw new UnsupportedOperationException("Not supported yet."); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/GeneratedMethodBody
                if (JOptionPane.showConfirmDialog(rootPane, "¿Seguro que desea descartar la imágen seleccionada?", "Eliminar imágen", JOptionPane.WARNING_MESSAGE) == JOptionPane.OK_OPTION) {
                    imgFondo2 = new ImagenFondo2(new File("src/GUI/imgs/placeholder-img.png").getAbsoluteFile(), 1.0f);
                    jPanelImagenFondoArrastable1.setFondoImg(imgFondo2);
                    jPanelImagenFondoArrastable1.repaint();
                }
            }

            //Si hacemos doble clic, mostrar el diálogo para cambiar de imágen y hacerlo si aceptamos.
            @Override
            public void toggleDblClick() {
                //throw new UnsupportedOperationException("Not supported yet."); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/GeneratedMethodBody
                JFileChooser elegirFicherosWindow = new JFileChooser();
                int resultado = elegirFicherosWindow.showOpenDialog(NuevaPelicula.this);

                if (resultado == JFileChooser.APPROVE_OPTION) {
                    //Al aceptar, cambiar el texto de la ruta por la ruta elegida
                    File ficheroTmp = elegirFicherosWindow.getSelectedFile();
                    imgFondo2 = new ImagenFondo2(ficheroTmp.getAbsoluteFile(), 1.0f);
                    jPanelImagenFondoArrastable1.setFondoImg(imgFondo2);
                }
                if (resultado == JFileChooser.CANCEL_OPTION) {
                    //No hacer nada al cancelar
                }
            }
        });
    }
